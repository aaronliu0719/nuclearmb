	  			<td>印度是世界上少數有能力自行興建核電廠的國家，印度為了保護本國廠商的利益，因此設下了嚴格而不合理的條件來逼退外國承包商。印度在福島核災後表示核能擴增計畫不變在2030年前預定擴增至全國13%能源使用核能。</td>
