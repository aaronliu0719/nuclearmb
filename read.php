				但即令如此美國還是基於能源安全的觀點，宣佈即將蓋新的核能發電廠。美國內華達州的最終處置場由於政治力量的牽扯，命運多桀。但事實上美國還是持續對內華達州的最終處置場做持續評估中，並沒有放棄。<br>
