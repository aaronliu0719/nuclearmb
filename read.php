	<p>現有核電廠不予延役是馬總統的競選政見，但馬總統也指出，執行該政策是要符合前述三條件，經仔細分析若現有核電廠不延役則完全違反上述三條件。</p>

	<p>總統若改變政策准予現有核電廠延役，完全沒有違反競選政見，因為該政見是有先決條件的，既然先決條件無法達成，則該政見即為無效。</p>

	<p>吾人深刻盼望馬總統在深思熟慮後，以國家社稷為重，推動現有核電機組延役政策。</p>



