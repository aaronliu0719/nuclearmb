		<h1>
			<!-- 請使用包含域名的絕對路徑 -->
			<a href="//nuclearmb.org/read/hedian_hao_weixian?/meiyou_hedianchang_bei_dizhen_damage_guo">
				沒有核電廠被地震<span lang="en-GB">Damaged</span>過
			</a>
		</h1>

		<p class="author-date">
			作者：<a href="//taiwanenergy.blogspot.tw/">陳立誠</a>／2013年10月4日　星期五
		</p>
	</header>


	<p>馬總統在9月25日接見<!-- u.pn代表專名號，只是測試一下效果而已 --><u class="pn">世界核能協會</u>秘書長阿格尼塔·瑞斯（<span lang="sv">Agneta Rising</span>）以及其他3位核能專家。在向他們請益國際使用核能的趨勢時，馬總統表示：<q>按照台灣的國家政策，非核家園是未來目標，但沒辦法一步到位，必須在過程中努力發展替代能源，只是什麼時候替代能源才能取代核能，目前沒人能提出具體時間表<a id="ref-1" href="#cite-1">[1]</a>。</q>馬總統又指出，<q>台灣是<em>孤島型能源系統</em>，有98%的能源仰賴進口，無法向外購電，因此<em>能源安全與國家安全是畫等號的。</em></q></p>


	<figure>
		<blockquote>
			<p>台灣的國家政策是「確保核安、穩健減核、打造綠能低碳環境，逐步邁向非核家園」，但過程中必須做到3件事——不限電、維持合理電價，以及達成對國際社會的減碳承諾。</p>
		</blockquote>

		<figcaption>接見核能專家時，馬英九總統如是強調，過程中在在顯示出其對國家未來的憂心。</figcaption>
	</figure>


	<p>他也說明，<q>台灣的國家政策是『<strong>確保核安、穩健減核、打造綠能低碳環境，逐步邁向非核家園</strong>』，但過程中必須做到3件事——<strong>也就是不限電、維持合理電價，以及達成對國際社會的減碳承諾。</strong></q></p>

	<hr>

	<p>如果配合馬總統指出，要達到非核家園過程要符合「不限電，維持合理電價，達成對國際社會的減碳承諾」這三條件。個人對核一到核三屆齡除役的政策是否可能改變，燃起了無比希望。</p>

	<ol>
		<li>
			<h4>不限電</h4>
			<p>核一到核三每年發電400億度，佔我國總發電量的20%<a id="ref-2" href="#cite-2">[2]</a>。若這三電廠六部機屆齡除役，我國供電必有巨大缺口，完全違反不限電的條件。</p>
		</li>

		<li>
			<h4>維持合理電價</h4>
			<p>現有<strong>核電廠過去5年平均每度發電成本0.64元</strong>，不到燃煤發電成本（每度1.53元）<em>之半</em>，更只有燃氣發電成本（每度3.26元）的<em>五分之一</em><a id="ref-2-1" href="#cite-2">[2]</a>。若核一到核三除役，400億度的核電不論由燃煤或燃氣取代，發電成本必然暴增，與維持合理電價要件背道而馳。</p>
		</li>

		<li>
			<h4>達成對國際減碳承諾</h4>
			<p>核能電廠是無碳能源，若由燃煤取代，每年碳排增加3600萬噸（全國交通碳排不過3300萬噸）<a id="ref-3" href="#cite-3">[3]</a>。若由燃氣電廠取代，每年碳排1800萬噸（也超過全國交通碳排之半），何能達成國際減碳承諾？</p>
		</li>
	</ol>

	<hr>

	<p>仔細檢視馬總統所強調的非核家園三條件，都可看出若現有核電機組在運轉40年後除役，完全違反上述三條件。</p>


